# Lists

friends = ["Kevin", "Karen", "Jim", "Oscar", "Toby"]

##=== Access ===##

# get last element
print(friends[-1])

# get second last element
print(friends[-2])


# get all elements after a certain element

print(friends[1:])

# get a range of element
# Start: end (up to but not including)
print(friends[1:5])


##===  Modify  === ##
friends[1] = "Mike"
print(friends)


